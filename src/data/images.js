// Función helper para obtener URL de imagen
export function getImageUrl(section, key) {
  if (!section || !key) return null;

  if (Array.isArray(images[section])) {
    return images[section].find(img => img.alt.includes(key))?.src || null;
  }

  return images[section]?.[key] || null;
}

// Función para obtener todas las imágenes de una sección
